#' Determine the OOXML namespace tier for a function name.
#' @param fn Character scalar, function name (any case).
#' @return One of "legacy", "xlfn", or "xlws".
#' @keywords internal
.prefix_for <- function(fn) {
  fn_up <- toupper(fn)
  if (fn_up %in% .spaghetti_env$XLWS)             return("xlws")
  if (fn_up %in% .spaghetti_env$XLFN)             return("xlfn")
  if (fn_up %in% .spaghetti_env$LEGACY_WORKSHEET) return("legacy")
  if (fn_up %in% .spaghetti_env$LEGACY_XLM)       return("legacy")
  "xlfn"   # unknown: conservative default
}

#' Strip any OOXML namespace prefix from a function name.
#' @param fn Character scalar.
#' @return Clean name without _xlfn., _xlfn._xlws., or _xlpm. prefix.
#' @keywords internal
.strip_prefix <- function(fn) {
  fn <- sub("^_xlfn\\._xlws\\.", "", fn)
  fn <- sub("^_xlfn\\.",         "", fn)
  fn <- sub("^_xlpm\\.",         "", fn)
  fn
}

