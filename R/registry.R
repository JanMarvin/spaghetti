#' Find the closest known function name(s) to an unknown token.
#'
#' Uses generalised edit distance (utils::adist) over the worksheet-function
#' registry (LEGACY_WORKSHEET ∪ XLFN ∪ XLWS). XLM macro names are excluded
#' from suggestions since user typos in worksheet contexts are almost
#' never typos of XLM names.
#'
#' Returns up to `n` suggestions, or character(0) if the closest match is
#' more than `max_dist` edits away.
#'
#' @param fn       Unknown function name (uppercase).
#' @param n        Maximum number of suggestions to return (default 3).
#' @param max_dist Maximum edit distance to consider a useful suggestion (default 3).
#' @return Character vector of suggestions, possibly empty.
#' @keywords internal
.spell_suggest <- function(fn, n = 3L, max_dist = 3L) {
  candidates <- c(
    .spaghetti_env$LEGACY_WORKSHEET,
    .spaghetti_env$XLFN,
    .spaghetti_env$XLWS
  )
  dists <- utils::adist(fn, candidates)[1L, ]
  ord   <- order(dists)
  keep  <- ord[seq_len(min(n, length(ord)))]
  keep  <- keep[dists[keep] <= max_dist]
  if (length(keep) == 0L) return(character(0))
  candidates[keep]
}

#' Emit a warning for an unknown function name, with spelling suggestions.
#'
#' Called from .transform_to_xml() when warn_unknown = TRUE.
#'
#' @param fn Unknown function name (uppercase).
#' @keywords internal
.warn_unknown_fn <- function(fn) {
  suggestions <- .spell_suggest(fn)
  if (length(suggestions) > 0L) {
    warning(
      "Unknown function: ", fn,
      ". Did you mean: ", paste(suggestions, collapse = ", "), "?",
      call. = FALSE
    )
  } else {
    warning(
      "Unknown function: ", fn,
      ". It will be prefixed with _xlfn. but may not be recognised.",
      call. = FALSE
    )
  }
}
